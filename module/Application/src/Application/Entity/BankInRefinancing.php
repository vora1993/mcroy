<?php
namespace Application\Entity;

class BankInRefinancing implements BankInRefinancingInterface
{
	protected $id;
    protected $name;    
    
    public function getId()
    {
        return $this->id;
    }
    public function setId($id)
    {
        $this->id = (int) $id;
        return $this;
    }
    
    public function getName()
    {
        return $this->name;
    }
    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }
}